#include <cstdio>
#define N 300

int ans[ N ] = {1};

void setup(int n)
{
	int fac = 1 ;
	for( int i=1 ; i<n ; i++ )
	{
		fac *= i ;
		while( fac%10==0 )
			fac /= 10 ;
		fac %= 10 ;
		ans[i] = fac ;
		printf("%3d = %d\t", i , ans[i] );
	}
}

int main()
{
	setup(N);

	int t;
	scanf("%d", &t );
	while( t-- )
	{
		long long int n;
		scanf("%lld", &n );
		if( n>41 )
			printf("%d\n", ans[ (n-2)%40 + 2 ] );
		else
			printf("%d\n", ans[n] );
	}
	return 0;
}
